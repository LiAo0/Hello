public class Test01 {
    public static void main(String[] args) {
        System.out.println("16 << 1 ：" + (16 << 1));
        System.out.println("16 >> 3 ：" + (16 >> 3));
        System.out.println("1 >> 1 ：" + (1 >> 1));
        System.out.println("16 >>> 2 ：" + (16 >>> 2));
        System.out.println("-16 << 2 ：" + (-16 << 2));
    }
}